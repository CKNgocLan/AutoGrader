package student;
import java.awt.Color;

public class ThemeColor {
	Color bgLight, bgDark, accent, buttonBg, buttonHover;

    ThemeColor(Color bgLight, Color bgDark, Color accent, Color buttonBg, Color buttonHover) {
        this.bgLight = bgLight;
        this.bgDark = bgDark;
        this.accent = accent;
        this.buttonBg = buttonBg;
        this.buttonHover = buttonHover;
    }
}
